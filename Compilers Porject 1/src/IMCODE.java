
public class IMCODE {

	String L,O, OP1,OP2,R,C;
	int sub, S;
	
	
	public IMCODE(String Line, String Operation, String Operand1, String Operand2, String Result, int SubScope, int Scope, String t){
		L = Line;
		O = Operation;
		OP1 = Operand1;
		OP2 = Operand2;
		R = Result;
		sub = SubScope;
		S = Scope;
		C = t;
	}
}
